void	ft_putchar(char c);

void	my_putstr(char *str)
{
	int i;

	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	my_putstr(argv[0]);
	return (0);
}
