void	ft_putchar(char c);

void	my_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char *argv[])
{
	int i;

	if (argc == 1)
		return (0);
	i = 1;
	while (i < argc)
	{
		my_putstr(argv[i]);
		ft_putchar('\n');
		i++;
	}
	return (0);
}
