int	is_alpha(char c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	return (0);
}

int	ft_str_is_alpha(char *str)
{
	if (str[0] == '\0')
		return (1);
	return (is_alpha(str[0]) && ft_str_is_alpha(&str[1]));
}
